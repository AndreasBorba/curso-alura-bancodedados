    (CASE WHEN TO_CHAR(DATA_DE_NASCIMENTO, 'YYYY') <= 1990 THEN 'IDOSO'
    WHEN TO_CHAR(DATA_DE_NASCIMENTO, 'YYYY') >= 1990 AND TO_CHAR(DATA_DE_NASCIMENTO, 'YYYY') <= 1995  THEN 'JOVÉM'
