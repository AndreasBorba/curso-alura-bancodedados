-- Aplicação do case

-- Colocamos uma condicional e a partir de um determinado valor apresentamos um resultado

SELECT CODIGO_DO_PRODUTO, QUANTIDADE, PRECO,
    (CASE WHEN PRECO <= 10 THEN 'PREÇO BAIXO'
    WHEN PRECO > 10 AND PRECO <= 20 THEN 'PREÇO MÉDIO'
    WHEN PRECO > 20 AND PRECO <= 30 THEN 'PREÇO ALTO'
    ELSE 'PREÇO IMPRATICAVEL'
    END) AS STATUS_PRECO
FROM ITENS_NOTAS_FISCAIS ORDER BY PRECO ASC;

