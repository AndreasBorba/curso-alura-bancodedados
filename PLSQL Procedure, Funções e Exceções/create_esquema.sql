CREATE TABLE TAB_SEGMERCADO(
ID NUMBER(5) GENERATED BY DEFAULT AS IDENTITY,
DESCRICAO VARCHAR(100),
PRIMARY KEY(ID)
);

CREATE TABLE TAB_CLIENTE(
ID NUMBER(5) GENERATED BY DEFAULT AS IDENTITY,
RAZAO_SOCIAL VARCHAR(100),
CNPJ VARCHAR(20),
SEGMERCADO_ID NUMBER(5),
DATA_INCLUSAO DATE,
FATURAMENTO_PREVISTO NUMBER(10,2),
CATEGORIA VARCHAR(20),
PRIMARY KEY(ID),
FOREIGN KEY (SEGMERCADO_ID) REFERENCES TAB_SEGMERCADO (ID)
);

